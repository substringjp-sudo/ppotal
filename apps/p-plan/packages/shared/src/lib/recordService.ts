/**
 * ─── 여행기 (Travelog) 서비스 ────────────────────────────────────
 * 사용자가 발자취를 바탕으로 일부 거점만 포함시켜 직접 발행하기를 선택한 경우에 한해, 
 * 서버와 동기화되고 웹으로 노출됩니다.
 */
/**
 * 새로운 여행기 저장 또는 업데이트
 */
export const saveTravelog = async (travelog: Travelog) => {
    if (!auth.currentUser || auth.currentUser.uid !== travelog.userId) {
        throw new Error("Authentication mismatch for travelog");
    }
    try {
        const ref = doc(db, TRAVELOGS_COLLECTION, travelog.id);
        const dataToSave = {
            ...travelog,
            updatedAt: new Date().toISOString() // 항상 최신 수정 일자 반영
        };
        await setDoc(ref, removeUndefined(dataToSave), { merge: true });
    } catch (error) {
        console.error("[RecordService] Error saving travelog:", error);
        throw error;
    }
};

/**
 *特定の 사용자의 모든 여행기 조회
 */
export const getUserTravelogs = async (userId: string, isPublicOnly: boolean = false): Promise<Travelog[]> => {
    try {
        let q = query(
            collection(db, TRAVELOGS_COLLECTION),
            where("userId", "==", userId),
            orderBy("createdAt", "desc")
        );

        if (isPublicOnly) {
            q = query(q, where("status", "==", "published"));
        }

        const snap = await getDocs(q);
        return snap.docs.map(doc => doc.data() as Travelog);
    } catch (error) {
        console.error("[RecordService] Error getting user travelogs:", error);
        return [];
    }
};

/**
 * 특정 여행기 상세 조회
 */
export const getTravelog = async (travelogId: string): Promise<Travelog | null> => {
    try {
        const docRef = doc(db, TRAVELOGS_COLLECTION, travelogId);
        const docSnap = await getDoc(docRef);
        return docSnap.exists() ? (docSnap.data() as Travelog) : null;
    } catch (error) {
        console.error("[RecordService] Error getting travelog:", error);
        throw error;
    }
};

/**
 * 여행기 삭제
 */
export const deleteTravelog = async (travelogId: string, authorId: string) => {
    if (!auth.currentUser || auth.currentUser.uid !== authorId) {
        throw new Error("Authentication mismatch for deletion");
    }
    try {
        await deleteDoc(doc(db, TRAVELOGS_COLLECTION, travelogId));
    } catch (error) {
        console.error("[RecordService] Error deleting travelog:", error);
        throw error;
    }
};

/**
 * 사용자의 여행기 목록을 실시간으로 구독합니다.
 */
export const subscribeToUserTravelogs = (userId: string, onUpdate: (travelogs: Travelog[]) => void) => {
    const q = query(
        collection(db, TRAVELOGS_COLLECTION),
        where("userId", "==", userId),
        orderBy("createdAt", "desc")
    );

    return onSnapshot(q, (snapshot) => {
        const travelogs = snapshot.docs.map(doc => doc.data() as Travelog);
        onUpdate(travelogs);
    }, (error) => {
        console.error("[RecordService] Error subscribing to travelogs:", error);
    });
};

/**
 * 트래블로그 타임라인을 분석하여 가장 빈도가 높은 대표 지역을 추출합니다.
 */
import { getRegionNamesByIds } from './region-service';
import { RegionIds } from '../types/common';

export const calculateTravelogRepresentativeRegion = async (travelog: Travelog) => {
    const counts: Record<string, { count: number, ids: RegionIds }> = {};
    
    if (!travelog.timeline) return null;

    for (const day of travelog.timeline) {
        if (!day.events) continue;
        for (const event of day.events) {
            // location 정보가 있고 국가 ID가 최소한이라도 있어야 함
            if (event.location?.countryId) {
                const cityPart = event.location.cityId || 'none';
                const prefPart = event.location.prefectureId || 'none';
                const key = `${event.location.countryId}_${prefPart}_${cityPart}`;
                
                if (!counts[key]) {
                    counts[key] = { 
                        count: 0, 
                        ids: { 
                            countryId: event.location.countryId,
                            prefectureId: event.location.prefectureId,
                            cityId: event.location.cityId
                        }
                    };
                }
                counts[key].count++;
            }
        }
    }
    
    // 가장 높은 빈도 찾기
    let winnerIds: RegionIds | null = null;
    let max = 0;
    
    for (const key in counts) {
        if (counts[key].count > max) {
            max = counts[key].count;
            winnerIds = counts[key].ids;
        }
    }
    
    if (!winnerIds) return null;
    
    // 지역 명칭 확보
    const names = await getRegionNamesByIds(winnerIds);
    return {
        ...winnerIds,
        ...names,
        frequency: max
    };
};
