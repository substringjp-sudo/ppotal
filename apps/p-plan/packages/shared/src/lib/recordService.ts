/**
 * 여행 계획(Trip)을 여행 기록(Travelog)으로 변환합니다.
 * 숙소 체크인/아웃, 항공 대기 시간 등 계획용 물류 정보를 제거하고 핵심 일정만 추출합니다.
 */
export const convertTripToTravelog = (trip: Trip, userId: string): Travelog => {
    const now = new Date().toISOString();
    
    // 타임라인 변환
    const timeline: TravelogDailyPlan[] = (trip.dailyTimeline || []).map(day => ({
        day: day.day,
        date: day.date,
        events: (day.events || [])
            .filter(event => {
                // 물류 전용 자동 생성 이벤트(체크인/아웃 등)는 제외하거나 간소화
                if (event.isAutoGenerated && (event.autoGeneratedType === 'check-in' || event.autoGeneratedType === 'check-out')) {
                    return false;
                }
                return true;
            })
            .map(event => ({
                id: event.id,
                title: event.title,
                type: 'activity', // 여행 계획에서 가져온 것은 기본적으로 활동으로 처리
                startTime: event.startTime,
                endTime: event.endTime,
                location: event.location,
                memo: event.memo,
                imageUrls: event.imageUrls,
                mainCategory: event.mainCategory,
                subCategory: event.subCategory,
                isPointInTime: event.isPointInTime
            }))
    }));

    return {
        id: trip.id,
        tripId: trip.id,
        userId: userId,
        title: trip.title,
        summary: trip.memo || '',
        theme: trip.theme || '힐링',
        memberCounts: trip.memberCounts || { me: 1, partner: 0, family: 0, friends: 0 },
        status: 'draft',
        isPublic: false,
        sourceContext: 'plan_only',
        timeline: timeline,
        sections: [
            {
                id: generateId(),
                type: 'text',
                content: '',
                contentJson: {
                    type: 'doc',
                    content: [
                        { type: 'heading', attrs: { level: 2 }, content: [{ type: 'text', text: '여행의 시작' }] },
                        { type: 'paragraph', content: [{ type: 'text', text: '소중한 여행의 기억을 남겨보세요.' }] }
                    ]
                }
            }
        ],
        createdAt: now,
        updatedAt: now
    };
};

